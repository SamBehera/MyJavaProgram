class Elseif
{
	public static void main(String[] Arg)
	{
	int num = 28;
	{
	if (num<25)
	{
	System.out.println("num less then 25 " + num);
	}
else if (num > 35)
	{
	System.out.println("num great then 35 " + num);
	}	
else if (num <30)
	{
	System.out.println("num less then 30 and greater than 25" + num);
	}
else
	{
	System.out.println("else block executed");
	}
	}
	}
}